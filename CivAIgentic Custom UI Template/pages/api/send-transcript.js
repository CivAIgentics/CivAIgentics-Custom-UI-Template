import { Resend } from 'resend';

const resend = new Resend(process.env.RESEND_API_KEY);

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  const { to, transcript, conversationId } = req.body;

  if (!to || !transcript) {
    return res.status(400).json({ error: 'Missing required fields' });
  }

  // Basic email validation
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  if (!emailRegex.test(to)) {
    return res.status(400).json({ error: 'Invalid email address' });
  }

  try {
    const { data, error } = await resend.emails.send({
      from: 'Jacky 2.0 <askjacky@midlandtexas.gov>',
      to: [to],
      subject: 'Your Conversation with Jacky 2.0 - City of Midland',
      html: `
        <!DOCTYPE html>
        <html>
        <head>
          <style>
            body { font-family: Arial, sans-serif; line-height: 1.6; color: #333; }
            .container { max-width: 600px; margin: 0 auto; padding: 20px; }
            .header { background: #00599c; color: white; padding: 20px; text-align: center; border-radius: 8px 8px 0 0; }
            .content { background: #f9fafb; padding: 20px; border: 1px solid #e5e7eb; }
            .message { background: white; padding: 12px; margin: 10px 0; border-radius: 8px; border-left: 4px solid #00599c; }
            .message.user { border-left-color: #78b9e8; }
            .message-type { font-weight: bold; color: #00599c; margin-bottom: 4px; }
            .message.user .message-type { color: #78b9e8; }
            .timestamp { font-size: 12px; color: #666; margin-top: 4px; }
            .footer { background: #f9fafb; padding: 20px; text-align: center; border: 1px solid #e5e7eb; border-top: none; border-radius: 0 0 8px 8px; font-size: 12px; color: #666; }
          </style>
        </head>
        <body>
          <div class="container">
            <div class="header">
              <h1>ðŸ¤– Jacky 2.0 - City of Midland</h1>
              <p>Your Conversation Transcript</p>
            </div>
            <div class="content">
              <p>Thank you for chatting with Jacky! Here's a transcript of your conversation:</p>
              ${transcript.map(msg => `
                <div class="message ${msg.type}">
                  <div class="message-type">${msg.type === 'agent' ? 'ðŸ¤– Jacky 2.0' : 'ðŸ‘¤ You'}</div>
                  <div>${msg.content}</div>
                  <div class="timestamp">${new Date(msg.timestamp).toLocaleString()}</div>
                </div>
              `).join('')}
            </div>
            <div class="footer">
              <p><strong>City of Midland, Texas</strong></p>
              <p>This transcript was generated by Jacky 2.0, your AI assistant.</p>
              <p>For more information, visit <a href="https://www.midlandtexas.gov">midlandtexas.gov</a></p>
              ${conversationId ? `<p style="color: #999;">Conversation ID: ${conversationId}</p>` : ''}
            </div>
          </div>
        </body>
        </html>
      `,
    });

    if (error) {
      console.error('Resend error:', error);
      return res.status(400).json({ error: error.message });
    }

    console.log('Email sent successfully:', data);
    return res.status(200).json({ success: true, messageId: data.id });
  } catch (error) {
    console.error('Send email error:', error);
    return res.status(500).json({ error: 'Failed to send email' });
  }
}
